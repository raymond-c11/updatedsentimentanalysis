# The imports json, re, math are now in the setup cell: GwvdzM-iy562
# Define batch size (using the existing batch_size variable)
num_batches_openai = math.ceil(len(ambiguous_df) / batch_size)
openai_predicted_sentiments = []

print(f"Processing {len(ambiguous_df)} ambiguous tweets in {num_batches_openai} batches of {batch_size} tweets each for OpenAI.")

# Loop through ambiguous_df in batches
for i in range(num_batches_openai):
    start_index = i * batch_size
    end_index = min((i + 1) * batch_size, len(ambiguous_df))
    current_batch_df_openai = ambiguous_df.iloc[start_index:end_index].copy() # Use .copy() to avoid SettingWithCopyWarning

    combined_tweet_bodies_batch_openai = ""
    # Build the combined tweet body string for the current batch
    for j, text_sample in enumerate(current_batch_df_openai['body']):
        # Keep Tweet ID sequential within the batch as the AI processes each batch independently.
        combined_tweet_bodies_batch_openai += f"### TWEET_START ###\nTweet ID: {j+1}\nTweet Text: {text_sample}\n### TWEET_END ###\n"

    print(f"\nSending batch {i+1}/{num_batches_openai} (tweets {start_index+1} to {end_index}) to the AI.")

    # Make a single call to the AI with the combined text for the current batch.
    raw_ai_response_string_openai = judge_single(model=MODEL, prompt=PROMPT, sample=combined_tweet_bodies_batch_openai)

    if raw_ai_response_string_openai:
        try:
            # Extract sentiment words from the AI's response using regex.
            # The AI is expected to return a string like: [neutral, neutral, ...]
            sentiment_words_openai = re.findall(r'([a-zA-Z]+)', raw_ai_response_string_openai)
            if sentiment_words_openai:
                # Directly use the extracted words as the parsed sentiments,
                # ensuring they are lowercase as per the prompt's output example.
                parsed_sentiments_openai = [s.lower() for s in sentiment_words_openai]
            else:
                print(f"Warning: No sentiment words found in AI response for ambiguous batch {i+1}: '{raw_ai_response_string_openai}'.")
                parsed_sentiments_openai = []

            if isinstance(parsed_sentiments_openai, list) and len(parsed_sentiments_openai) == len(current_batch_df_openai):
                openai_predicted_sentiments.extend(parsed_sentiments_openai)
                print(f"Successfully parsed AI response for ambiguous batch {i+1}. Found {len(parsed_sentiments_openai)} sentiments.")
            else:
                print(f"Error: Parsed AI response for ambiguous batch {i+1} is not a list of the expected length ({len(current_batch_df_openai)}). Got {len(parsed_sentiments_openai)}. Filling with 'Failed to answer' for this batch.")
                openai_predicted_sentiments.extend(['Failed to answer'] * len(current_batch_df_openai))
        except Exception as e:
            # Catch any unexpected errors during AI response parsing for the current batch
            print(f"An unexpected error occurred during AI response parsing for ambiguous batch {i+1}: {e}. Filling with 'Failed to answer' for this batch.")
            openai_predicted_sentiments.extend(['Failed to answer'] * len(current_batch_df_openai))
    else:
        print(f"AI did not return a response for ambiguous batch {i+1}. Filling with 'Failed to answer' for this batch.")
        openai_predicted_sentiments.extend(['Failed to answer'] * len(current_batch_df_openai))

# After processing all ambiguous batches, assign the collected predicted sentiments to ambiguous_df
