def get_finvader_sentiment(text):
    vs = analyzer.polarity_scores(text)
    return vs['compound']

new_df['vader_score'] = new_df['body'].apply(get_finvader_sentiment)

# Classify initial sentiment based on standard VADER thresholds
def classify_vader_sentiment(score):
    if score >= 0.05:
        return 'positive'
    elif score <= -0.05:
        return 'negative'
    else:
        return 'neutral'

new_df['initial_sentiment'] = new_df['vader_score'].apply(classify_vader_sentiment)

