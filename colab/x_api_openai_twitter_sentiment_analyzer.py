def plot_distribution(df, pred_col, true_col, dataset_name):
    fig, axes = plt.subplots(1, 2, figsize=(12, 4))
    fig.suptitle(f"{dataset_name} — Label Distribution", fontsize=14, fontweight="bold")
    colors = {"Positive": "#2ecc71", "Negative": "#e74c3c", "Neutral": "#95a5a6"}

    for ax, col, title in zip(axes,
                               [pred_col, true_col],
                               ["GPT Judge Predictions", "Ground Truth"]):
        counts = df[col].value_counts()
        bar_colors = [colors.get(l, "#3498db") for l in counts.index]
        ax.bar(counts.index, counts.values, color=bar_colors)
        ax.set_title(title)
        ax.set_xlabel("Sentiment")
        ax.set_ylabel("Count")
        for i, v in enumerate(counts.values):
            ax.text(i, v + max(counts.values) * 0.01, str(v), ha="center", fontsize=10)

    plt.tight_layout()
    plt.savefig(f"{dataset_name.replace(' ', '_')}_LLMJudge_distribution.png", dpi=150)
    plt.show()
    print("  → Saved distribution plot.\n")


def plot_confusion(df, pred_col, true_col, dataset_name, labels):
    cm = confusion_matrix(df[true_col], df[pred_col], labels=labels, normalize="true")
    fig, ax = plt.subplots(figsize=(6, 5))
    sns.heatmap(cm, annot=True, fmt=".2f", cmap="Greens",
                xticklabels=labels, yticklabels=labels, ax=ax)
    ax.set_title(f"{dataset_name} — Confusion Matrix (normalised)", fontweight="bold")
    ax.set_xlabel("Predicted")
    ax.set_ylabel("Ground Truth")
    plt.tight_layout()
    plt.savefig(f"{dataset_name.replace(' ', '_')}_LLMJudge_confusion.png", dpi=150)
    plt.show()
    print("  → Saved confusion matrix.\n")


def evaluate(df, pred_col, true_col, dataset_name, labels) -> float:
    acc = accuracy_score(df[true_col], df[pred_col])
